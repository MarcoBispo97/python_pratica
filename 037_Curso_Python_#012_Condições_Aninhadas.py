# Escreva um numero inteiro qualquer e peça para o usuário escolher qual será a base de conversão
# 1-Binário
# 2-Octal
# 3-Hexadecimal
n_inteiro = int(input('Digite um número inteiro: '))
print("Esse número em binário é: ", bin(n_inteiro))
print("Esse número em octal é: ", oct(n_inteiro))
print("Esse número em binário é: ", hex(n_inteiro))
